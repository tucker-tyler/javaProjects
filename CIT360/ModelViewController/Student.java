public class Student {
   private String rollNumber;
   private String name;
   
   public String getRollNumber() {
      return rollNumber;
   }
   
   public void setRollNumber(String rollNumber) {
      this.rollNumber = rollNumber;
   }
   
   public String getName() {
      return name;
   }
   
   public void setName(String name) {
      this.name = name;
   }
}